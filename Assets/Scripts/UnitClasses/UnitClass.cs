public abstract class UnitClass
{
    public abstract float getBaseHealth();
    public abstract float getWalkSpeed();
    public abstract float getRunSpeed();
    public abstract float getStaticAccuracy();
    public abstract float getMovingAccuracy();
    public abstract float getRunningDodge();
    public abstract float getWalkingDodge();
    public abstract float getStaticDodge();
    public abstract float getFireRate();

    public abstract void usePower();
}